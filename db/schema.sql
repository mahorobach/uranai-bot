-- ─── ユーザーテーブル ────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS users (
  id            UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  line_user_id  VARCHAR(255) UNIQUE NOT NULL,
  name          VARCHAR(255),
  date_of_birth DATE,
  created_at    TIMESTAMP DEFAULT NOW(),
  updated_at    TIMESTAMP DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_users_line_user_id ON users(line_user_id);

-- ─── 占い結果キャッシュテーブル ──────────────────────────────────
-- キャッシュキー: name + date (同じ命式・同じ氏名の2人目以降は即返却)
CREATE TABLE IF NOT EXISTS fortune_cache (
  id                 BIGSERIAL PRIMARY KEY,
  name               VARCHAR(255) NOT NULL,
  date               DATE NOT NULL,
  fortune_text       TEXT NOT NULL,
  year_stem_branch   VARCHAR(4),
  month_stem_branch  VARCHAR(4),
  day_stem_branch    VARCHAR(4),
  element            VARCHAR(2),
  tsuhen_year        VARCHAR(4),
  tsuhen_month       VARCHAR(4),
  junishi_year       VARCHAR(6),
  junishi_month      VARCHAR(6),
  junishi_day        VARCHAR(6),
  surei_number       INTEGER,
  created_at         TIMESTAMP DEFAULT NOW(),
  UNIQUE(name, date)
);
CREATE INDEX IF NOT EXISTS idx_fortune_cache_date ON fortune_cache(date);
CREATE INDEX IF NOT EXISTS idx_fortune_cache_name ON fortune_cache(name);

-- ─── ユーザーの占い履歴 ──────────────────────────────────────────
CREATE TABLE IF NOT EXISTS user_fortunes (
  id            BIGSERIAL PRIMARY KEY,
  user_id       UUID NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  name          VARCHAR(255) NOT NULL,
  date_of_birth DATE NOT NULL,
  fortune_text  TEXT NOT NULL,
  metadata      JSONB,
  is_paid       BOOLEAN DEFAULT FALSE,
  created_at    TIMESTAMP DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_user_fortunes_user_id    ON user_fortunes(user_id);
CREATE INDEX IF NOT EXISTS idx_user_fortunes_created_at ON user_fortunes(created_at);

-- ─── 決済テーブル ────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS payments (
  id                 BIGSERIAL PRIMARY KEY,
  user_id            UUID NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  stripe_payment_id  VARCHAR(255) UNIQUE,
  amount_jpy         INTEGER NOT NULL,
  status             VARCHAR(50) DEFAULT 'pending',
  fortune_id         BIGINT REFERENCES user_fortunes(id),
  created_at         TIMESTAMP DEFAULT NOW(),
  updated_at         TIMESTAMP DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_payments_user_id ON payments(user_id);
CREATE INDEX IF NOT EXISTS idx_payments_status  ON payments(status);

