/** LINEに届かないマークダウン記号を除去 */
function stripMarkdown(text) {
  return text
    .replace(/^#{1,6}\s*/gm, '')
    .replace(/\*\*(.+?)\*\*/g, '$1')
    .replace(/\*(.+?)\*/g, '$1')
    .replace(/^[-*]\s+/gm, '・')
    .replace(/\[(.+?)\]\(.+?\)/g, '$1')
    .trim();
}

